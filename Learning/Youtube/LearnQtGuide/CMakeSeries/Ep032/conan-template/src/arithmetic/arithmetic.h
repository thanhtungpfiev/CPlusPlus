// arithmetic.hpp

#pragma once

namespace Arithmetic {

class Math {
 public:
  /**
      Function to add two numbers.
      @param a the first parameter
      @param b the second parameter
      @result The result
  */
  int add(int a, int b);

  /**
      Function to subtract two numbers.
      @param a the first parameter
      @param b the second parameter
      @result The result
  */
  int sub(int a, int b);

  /**
      Function to multiply two numbers.
      @param a the first parameter
      @param b the second parameter
      @result The result
  */
  int mult(int a, int b);
};

}  // namespace Arithmetic